package com.test.entity;

public class Wedding {
        private int weddingId;
        private String weddingName;
        private String weddingStyle;
        private int weddingPrice;
        private String weddingIntro;
        private String weddingHotel;
        private String weddingPic;

        public int getWeddingId() {
            return weddingId;
        }

        public void setWeddingId(int weddingId) {
            this.weddingId = weddingId;
        }

        public String getWeddingName() {
            return weddingName;
        }

        public void setWeddingName(String weddingName) {
            this.weddingName = weddingName;
        }

        public String getWeddingStyle() {
            return weddingStyle;
        }

        public void setWeddingStyle(String weddingStyle) {
            this.weddingStyle = weddingStyle;
        }

        public int getWeddingPrice() {
            return weddingPrice;
        }

        public void setWeddingPrice(int weddingPrice) {
            this.weddingPrice = weddingPrice;
        }

        public String getWeddingIntro() {
            return weddingIntro;
        }

        public void setWeddingIntro(String weddingIntro) {
            this.weddingIntro = weddingIntro;
        }

        public String getWeddingHotel() {
            return weddingHotel;
        }

        public void setWeddingHotel(String weddingHotel) {
            this.weddingHotel = weddingHotel;
        }

        public String getWeddingPic() {
            return weddingPic;
        }

        public void setWeddingPic(String weddingPic) {
            this.weddingPic = weddingPic;
        }
}
