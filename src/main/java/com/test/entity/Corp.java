package com.test.entity;

public class Corp {
    private String corpName;
    private String corpContact;
    private String corpEmail;
    private String corpAddress;
    private String corpIntro;


    public String getCorpName() {
        return corpName;
    }

    public void setCorpName(String corpName) {
        this.corpName = corpName;
    }

    public String getCorpContact() {
        return corpContact;
    }

    public void setCorpContact(String corpContact) {
        this.corpContact = corpContact;
    }

    public String getCorpEmail() {
        return corpEmail;
    }

    public void setCorpEmail(String corpEmail) {
        this.corpEmail = corpEmail;
    }

    public String getCorpAddress() {
        return corpAddress;
    }

    public void setCorpAddress(String corpAddress) {
        this.corpAddress = corpAddress;
    }

    public String getCorpIntro() {
        return corpIntro;
    }

    public void setCorpIntro(String corpIntro) {
        this.corpIntro = corpIntro;
    }
}
